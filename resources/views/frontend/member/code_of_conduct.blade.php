			<strong><p>Code of Professional Conduct</p></strong>
			<p class="text-gap">Members of the Institute are expected to observe the highest standards of professional conduct and ethical behavior in all their activities and to uphold the objectives of the institute.</p>
			<strong><p>In particular, members shall:</p></strong>
			<ul>
				<li>Abide by the letter and spirit of the Memorandum and Articles of Association of the Institute and any codes made by the Council of the Institute;</li>
				<li>At all times be cognisant of their responsibilities as professional persons towards the wider community;</li>
				<li>Maintain good corporate governance, management and efficient administration in their professional capacity as a Chartered Secretary;</li>
				<li>Exercise probity, honesty and diligence in carrying out their duties and responsibilities;</li>
				<li>Conduct all business dealings strictly according to all statutory rules and regulations;</li>
				<li>Safeguard the interests of their employers, colleagues or clients;</li>
				<li>Respect the confidentiality of information they encounter in the workplace and through their working relationships;</li>
				<li>Declare to their employers, where appropriate, any conflict of interest which, to the best of their knowledge, arises or has arisen;</li>
				<li>Exercise due care and diligence in performing their duties and ensure the currency of their knowledge, skills and technical competencies in relation to their professional activities; and</li>
				<li>Refrain from conduct or actions which may cause the image and reputation of the Institute to be tarnished. Members shall not:</li>
				<li>Enter into any agreement or undertake any activity which may be in conflict with the legitimate interests of their employers or clients, or which may prejudice the performance of their professional duties;</li>
				<li>Knowingly be a party to any illegal or unethical activities;</li>
				<li>Solicit or accept any advantages, whether in Bangladesh or elsewhere, when conducting business on behalf of their employers; or</li>
				<li>Use confidential information obtained in the performance of their duties for any personal gain or in a manner which would be detrimental to their employers or clients. Notification to Employer Members are encouraged to inform their employers at the time of employment that they are subject to the Code of Professional Conduct and to disciplinary proceedings in the event of not complying with the Code or with the Articles of Association. A copy of the Code may be provided to an employer if so requested.</li>
			</ul>
