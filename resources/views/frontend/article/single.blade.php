                <p>Institute of Chartered Secretaries of Bangladesh (ICSB) organized its 8th Convocation for the Qualified Chartered Secretaries who successfully completed their professional examinations and other requisites of the Institute. The ceremony took place at the Grand Ball Room of Radisson Blu Dhaka Water Garden on Sunday, December 12, 2021.</p>
                <p>Muhammad Abdul Mannan MP, Hon’ble Minister, Ministry of Planning, graced the occasion as the Chief Guest while Professor Mohammad Abdul Momen, Director, the Institute of Business Administration (IBA), University of Dhaka was present in the programme as Special Guest. Professor Mohammed Farashuddin, PhD, Chief Advisor & Founder Vice Chancellor, East West University and Former Governor, Bangladesh Bank was the Convocation Speaker.</p>
                <p>In his speech The Chief Guest Muhammad Abdul Mannan, MP, congratulated the Qualified Chartered Secretaries and mentioned that Chartered Secretaries have the great opportunity of serving the corporate world as Practicing Chartered Secretaries or as Company Secretaries to promote Corporate Governance Practice in Bangladesh. He also said that convocation is an important day for all graduates, parents, family and friends. This achievement is one that is seldom complete without the support of nearest well-wishers.”</p>
                <p>The special guest, Professor Mohammad Abdul Momen, said, “Bangladesh is on a tremendously progressive journey to become a developed nation.  We faced a lot of obstacles during the COVID-19 pandemic. The prudent efforts of our government is helping us to quickly overcome our shortcomings. Now Bangladesh has to attract a huge portion of the international market in order to stay relevant.” He said that Chartered Secretaries have a key role to play here, as they are in charge of ensuring the efficacy of the corporate governance mechanism. The Chartered Secretaries play the challenging and important role of bridging the gap between the management of a company, its board members and its shareholders.”</p>
                <p>The Convocation Speaker, Professor Mohammed Farashuddin, PhD, said that professional education is a tool that enriches the minds of the students with skills that are necessary to compete and succeed in a challenging world. The knowledge gained will also help to bring about and maintain peace and justice in society. He urged the newly qualified Chartered Secretaries to be the best ambassadors of the Institute in their professional field.</p>
                <p>The programme was chaired by Muzaffar Ahmed, FCMA, FCS, President of ICSB. He said, in his speech, that Convocation is a special day for newly qualified Chartered Secretaries. On this august and colorful occasion, he expressed his deepest appreciation to the faculty members, patrons, guardians and students for attending the convocation. He also said that attaining the ICSB qualification does not end the learning process. In fact, it is only the stepping stone on a new phase of learning and scope. The knowledge, skill and integrity will be tested and examined, no longer in the examination hall and class rooms, but in the highly competitive and challenging working environment.</p>
                <p>A good number of graduates took part in the convocation. A Convocation Profile was released by Md. Azizur Rahman FCS, Sr. Vice President of ICSB and Chairman, Education Committee, ICSB and Council Member of ICSB. Mohammad Sanaullah FCS, Immediate Past President of ICSB and Chairman, Examination Committee Introduced the Awardees.</p>
